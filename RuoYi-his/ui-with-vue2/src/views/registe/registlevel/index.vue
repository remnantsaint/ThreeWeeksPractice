<template>
  <div class="app-container">
    <el-form :model="queryParams" ref="queryForm" size="small" :inline="true" v-show="showSearch" label-width="68px">
      <el-form-item label="号别编码" prop="registcode">
        <el-input
          v-model="queryParams.registcode"
          placeholder="请输入号别编码"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="号别名称" prop="registname">
        <el-input
          v-model="queryParams.registname"
          placeholder="请输入号别名称"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="显示顺序号" prop="sequenceno">
        <el-input
          v-model="queryParams.sequenceno"
          placeholder="请输入显示顺序号"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="挂号费" prop="registfee">
        <el-input
          v-model="queryParams.registfee"
          placeholder="请输入挂号费"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="挂号限额" prop="registquota">
        <el-input
          v-model="queryParams.registquota"
          placeholder="请输入挂号限额"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="删除标记" prop="delmark">
        <el-input
          v-model="queryParams.delmark"
          placeholder="请输入删除标记"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item>
        <el-button type="primary" icon="el-icon-search" size="mini" @click="handleQuery">搜索</el-button>
        <el-button icon="el-icon-refresh" size="mini" @click="resetQuery">重置</el-button>
      </el-form-item>
    </el-form>

    <el-row :gutter="10" class="mb8">
      <el-col :span="1.5">
        <el-button
          type="primary"
          plain
          icon="el-icon-plus"
          size="mini"
          @click="handleAdd"
          v-hasPermi="['registe:registlevel:add']"
        >新增
        </el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="success"
          plain
          icon="el-icon-edit"
          size="mini"
          :disabled="single"
          @click="handleUpdate"
          v-hasPermi="['registe:registlevel:edit']"
        >修改
        </el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="danger"
          plain
          icon="el-icon-delete"
          size="mini"
          :disabled="multiple"
          @click="handleDelete"
          v-hasPermi="['registe:registlevel:remove']"
        >删除
        </el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="warning"
          plain
          icon="el-icon-download"
          size="mini"
          @click="handleExport"
          v-hasPermi="['registe:registlevel:export']"
        >导出
        </el-button>
      </el-col>
      <right-toolbar :showSearch.sync="showSearch" @queryTable="getList"></right-toolbar>
    </el-row>

    <el-table v-loading="loading" :data="registlevelList" @selection-change="handleSelectionChange">
      <el-table-column type="selection" width="55" align="center"/>
      <el-table-column label="ID主键" align="center" prop="id"/>
      <el-table-column label="号别编码" align="center" prop="registcode"/>
      <el-table-column label="号别名称" align="center" prop="registname"/>
      <el-table-column label="显示顺序号" align="center" prop="sequenceno"/>
      <el-table-column label="挂号费" align="center" prop="registfee"/>
      <el-table-column label="挂号限额" align="center" prop="registquota"/>
      <el-table-column label="删除标记" align="center" prop="delmark"/>
      <el-table-column label="操作" align="center" class-name="small-padding fixed-width">
        <template slot-scope="scope">
          <el-button
            size="mini"
            type="text"
            icon="el-icon-edit"
            @click="handleUpdate(scope.row)"
            v-hasPermi="['registe:registlevel:edit']"
          >修改
          </el-button>
          <el-button
            size="mini"
            type="text"
            icon="el-icon-delete"
            @click="handleDelete(scope.row)"
            v-hasPermi="['registe:registlevel:remove']"
          >删除
          </el-button>
        </template>
      </el-table-column>
    </el-table>

    <pagination
      v-show="total>0"
      :total="total"
      :page.sync="queryParams.pageNum"
      :limit.sync="queryParams.pageSize"
      @pagination="getList"
    />

    <!-- 添加或修改挂号级别对话框 -->
    <el-dialog :title="title" :visible.sync="open" width="500px" append-to-body>
      <el-form ref="form" :model="form" :rules="rules" label-width="80px">
        <el-form-item label="号别编码" prop="registcode">
          <el-input v-model="form.registcode" placeholder="请输入号别编码"/>
        </el-form-item>
        <el-form-item label="号别名称" prop="registname">
          <el-input v-model="form.registname" placeholder="请输入号别名称"/>
        </el-form-item>
        <el-form-item label="显示顺序号" prop="sequenceno">
          <el-input v-model="form.sequenceno" placeholder="请输入显示顺序号"/>
        </el-form-item>
        <el-form-item label="挂号费" prop="registfee">
          <el-input v-model="form.registfee" placeholder="请输入挂号费"/>
        </el-form-item>
        <el-form-item label="挂号限额" prop="registquota">
          <el-input v-model="form.registquota" placeholder="请输入挂号限额"/>
        </el-form-item>
        <el-form-item label="删除标记" prop="delmark">
          <el-input v-model="form.delmark" placeholder="请输入删除标记"/>
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button type="primary" @click="submitForm">确 定</el-button>
        <el-button @click="cancel">取 消</el-button>
      </div>
    </el-dialog>
  </div>
</template>

<script>
import {
  listRegistlevel,
  getRegistlevel,
  delRegistlevel,
  addRegistlevel,
  updateRegistlevel
} from "@/api/registe/registlevel";

export default {
  name: "Registlevel",
  data() {
    return {
      // 遮罩层
      loading: true,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 挂号级别表格数据
      registlevelList: [],
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        registcode: null,
        registname: null,
        sequenceno: null,
        registfee: null,
        registquota: null,
        delmark: null,
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        registcode: [
          {required: true, message: "号别编码不能为空", trigger: "blur"}
        ],
        registname: [
          {required: true, message: "号别名称不能为空", trigger: "blur"}
        ],
        sequenceno: [
          {required: true, message: "显示顺序号不能为空", trigger: "blur"}
        ],
        registquota: [
          {required: true, message: "挂号限额不能为空", trigger: "blur"}
        ],
        delmark: [
          {required: true, message: "删除标记不能为空", trigger: "blur"}
        ],
      }
    };
  },
  created() {
    this.getList();
  },
  methods: {
    /** 查询挂号级别列表 */
    getList() {
      this.loading = true;
      listRegistlevel(this.queryParams).then(response => {
        this.registlevelList = response.records;
        this.total = response.total;
        this.loading = false;
      });
    },
    // 取消按钮
    cancel() {
      this.open = false;
      this.reset();
    },
    // 表单重置
    reset() {
      this.form = {
        id: null,
        registcode: null,
        registname: null,
        sequenceno: null,
        registfee: null,
        registquota: null,
        delmark: null,
        createBy: null,
        createTime: null,
        updateBy: null,
        updateTime: null,
        remark: null,
        isDelete: null
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm");
      this.handleQuery();
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.id)
      this.single = selection.length !== 1
      this.multiple = !selection.length
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset();
      this.open = true;
      this.title = "添加挂号级别";
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset();
      const id = row.id || this.ids
      getRegistlevel(id).then(response => {
        this.form = response.data;
        this.open = true;
        this.title = "修改挂号级别";
      });
    },
    /** 提交按钮 */
    submitForm() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.id != null) {
            updateRegistlevel(this.form).then(response => {
              this.$modal.msgSuccess("修改成功");
              this.open = false;
              this.getList();
            });
          } else {
            addRegistlevel(this.form).then(response => {
              this.$modal.msgSuccess("新增成功");
              this.open = false;
              this.getList();
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const ids = row.id || this.ids;
      this.$modal.confirm('是否确认删除挂号级别编号为"' + ids + '"的数据项？').then(function () {
        return delRegistlevel(ids);
      }).then(() => {
        this.getList();
        this.$modal.msgSuccess("删除成功");
      }).catch(() => {
      });
    },
    /** 导出按钮操作 */
    handleExport() {
      this.download('registe/registlevel/export', {
        ...this.queryParams
      }, `registlevel_${new Date().getTime()}.xlsx`)
    }
  }
};
</script>
